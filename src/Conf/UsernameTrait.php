<?php

namespace yjHyperfAdminPligin\Email\Conf;

trait UsernameTrait
{
    private $username;

    /**
     * @return mixed
     */
    public function getUsername()
    {
        return $this->username;
    }

    /**
     * @param mixed $username
     */
    public function setUsername($username): void
    {
        $this->username = $username;
    }


}